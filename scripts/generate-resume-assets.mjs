import { mkdir, readdir, readFile, rm, unlink, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const siteUrl = "https://ccd97.github.io";
const generatedMarker = "Generated by scripts/generate-resume-assets.mjs";

const variants = JSON.parse(
  await readFile(path.join(rootDir, "src/data/pdfVariants.json"), "utf8"),
);
const portfolioData = JSON.parse(
  await readFile(path.join(rootDir, "src/data/portfolio.json"), "utf8"),
);

const personName = portfolioData.hero?.name ?? "Cyprien Dcunha";

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function validateVariant(variant) {
  if (!/^[a-z0-9][a-z0-9-]*$/.test(variant.slug)) {
    throw new Error(`Invalid resume variant slug: ${variant.slug}`);
  }
  if (!variant.label || !variant.file) {
    throw new Error(`Resume variant "${variant.slug}" must include label and file`);
  }
}

function titleFor(variant) {
  return variant.shareTitle ?? `${personName} — Resume (${variant.label})`;
}

function descriptionFor(variant) {
  return (
    variant.shareDescription ??
    `${personName} is a Senior Software Engineer with experience in distributed systems, backend platforms, cloud infrastructure, and applied GenAI.`
  );
}

function renderVariantPage(variant) {
  const title = escapeHtml(titleFor(variant));
  const description = escapeHtml(descriptionFor(variant));
  const pageUrl = `${siteUrl}/resume/${variant.slug}`;
  const pdfUrl = `/resumes/${variant.file}`;
  const pdfTitle = `${variant.label} Resume PDF`;

  return `<!DOCTYPE html>
<!-- ${generatedMarker}. Do not edit directly. -->
<html lang="en">
  <head>
    <meta charset="UTF-8" />
    <title>${title}</title>
    <meta
      name="viewport"
      content="width=device-width,height=device-height,initial-scale=1.0,viewport-fit=cover"
    />
    <meta name="google" value="notranslate" content="notranslate" />
    <meta name="author" content="${escapeHtml(personName)}" />
    <meta name="description" content="${description}" />
    <link rel="canonical" href="${pageUrl}" />
    <meta property="og:title" content="${title}" />
    <meta property="og:image" content="${siteUrl}/media/og-resume.png" />
    <meta property="og:description" content="${description}" />
    <meta property="og:type" content="profile" />
    <meta property="og:url" content="${pageUrl}" />
    <meta name="twitter:card" content="summary_large_image" />
    <meta name="twitter:title" content="${title}" />
    <meta name="twitter:description" content="${description}" />
    <meta name="twitter:image" content="${siteUrl}/media/og-resume.png" />
    <link rel="alternate" type="application/pdf" title="${escapeHtml(pdfTitle)}" href="${pdfUrl}" />
    <link rel="icon" type="image/png" href="/media/tabicon.ico" />
    <script>
      document.documentElement.classList.remove("dark");
    </script>
  </head>
  <body>
    <div id="root"></div>
    <noscript>
      <p>
        <a href="${siteUrl}${pdfUrl}">
          Download the ${escapeHtml(variant.label)} Resume PDF
        </a>
      </p>
    </noscript>
    <script type="module" src="/src/resume.tsx"></script>
  </body>
</html>
`;
}

function urlEntry(loc, changefreq, priority) {
  return `  <url>
    <loc>${loc}</loc>
    <changefreq>${changefreq}</changefreq>
    <priority>${priority}</priority>
  </url>`;
}

function renderSitemap() {
  const entries = [
    urlEntry(`${siteUrl}/`, "weekly", "1.0"),
    urlEntry(`${siteUrl}/resume.html`, "weekly", "0.9"),
    ...variants.map((variant) =>
      urlEntry(`${siteUrl}/resume/${variant.slug}`, "weekly", "0.8"),
    ),
    urlEntry(`${siteUrl}/resume.md`, "weekly", "0.9"),
    urlEntry(`${siteUrl}/llms.txt`, "weekly", "0.8"),
    urlEntry(`${siteUrl}/llms-full.txt`, "weekly", "0.8"),
    ...variants.map((variant) =>
      urlEntry(`${siteUrl}/resumes/${variant.file}`, "monthly", variant.slug === "latest" ? "0.8" : "0.7"),
    ),
  ];

  return `<?xml version="1.0" encoding="UTF-8"?>
<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">
${entries.join("\n")}
</urlset>
`;
}

for (const variant of variants) {
  validateVariant(variant);
}

const generatedResumeDir = path.join(rootDir, "resume");
const expectedLegacyPages = new Set(variants.map((variant) => `resume-${variant.slug}.html`));

for (const fileName of await readdir(rootDir)) {
  if (!/^resume-[\w-]+\.html$/.test(fileName) || expectedLegacyPages.has(fileName)) continue;

  const filePath = path.join(rootDir, fileName);
  const contents = await readFile(filePath, "utf8");
  if (contents.includes(generatedMarker)) {
    await unlink(filePath);
  }
}

for (const fileName of expectedLegacyPages) {
  const filePath = path.join(rootDir, fileName);
  try {
    const contents = await readFile(filePath, "utf8");
    if (contents.includes(generatedMarker)) {
      await unlink(filePath);
    }
  } catch (err) {
    if (err.code !== "ENOENT") throw err;
  }
}

await rm(generatedResumeDir, { recursive: true, force: true });

for (const variant of variants) {
  const variantDir = path.join(generatedResumeDir, variant.slug);
  await mkdir(variantDir, { recursive: true });
  await writeFile(path.join(generatedResumeDir, `${variant.slug}.html`), renderVariantPage(variant));
  await writeFile(path.join(variantDir, "index.html"), renderVariantPage(variant));
}

await writeFile(path.join(rootDir, "public/sitemap.xml"), renderSitemap());

console.log(`Generated ${variants.length} resume variant page(s) and public/sitemap.xml`);
